// Variáveis

int num1 = 30;
int num2 = 3;

// Divisão

Console.WriteLine(num1 / num2);

//Multiplicação

Console.WriteLine(num1 * num2);

// Subtração

Console.WriteLine(num1 - num2);

// Soma

Console.WriteLine(num1 + num2);

// Modulo (Resto da Divisão)

Console.WriteLine(num1 % num2);

// Incremento

num1++;
Console.WriteLine(num1);

// Decremento

num2--;
Console.WriteLine(num2);