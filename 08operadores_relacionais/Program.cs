int num1 = 5;
int num2 = 8;

//Maior
Console.WriteLine(num1 > num2);

// Maior ou igual

Console.WriteLine(num1 >= num2);

// Menor

Console.WriteLine(num1 < num2);

// Menor ou igual

Console.WriteLine(num1 <= num2);

// Igual

Console.WriteLine(num1 == num2);


// Diferente

Console.WriteLine(num1 != num2);